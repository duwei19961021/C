int main(){
    unsigned int a = 10;    // 0000 0000 0000 0000 0000 0000 0000 1010
    a =  a >> 3;            // 0000 0000 0000 0000 0000 0000 0000 0001
    printf("a = %d\n",a);   // 1,无符号右移补0

    int b = -5;
    // 原码: 1000 0000 0000 0000 0000 0000 0000 0101
    // 反码: 1111 1111 1111 1111 1111 1111 1111 1010
    // 补码: 1111 1111 1111 1111 1111 1111 1111 1011
    b = b << 3;
    // 补码: 1111 1111 1111 1111 1111 1111 1101 1000
    // 反码: 1111 1111 1111 1111 1111 1111 1101 0111
    // 原码: 1000 0000 0000 0000 0000 0000 0010 1000
    printf("b = %d\n",b);   // -40

    int c = -5;
    // 原码: 1000 0000 0000 0000 0000 0000 0000 0101
    // 反码: 1111 1111 1111 1111 1111 1111 1111 1010
    // 补码: 1111 1111 1111 1111 1111 1111 1111 1011
    c = c >> 3;
    // 补码: 1111 1111 1111 1111 1111 1111 1111 1111
    // 反码: 1111 1111 1111 1111 1111 1111 1111 1110
    // 原码: 1000 0000 0000 0000 0000 0000 0000 0001
    printf("c = %d\n",c);   // -1,有符号数右移时补符号位
    // c = -1
}

int DrinkSoda(int bottle){ //一瓶汽水一元，两空瓶换一瓶汽水,x为空瓶子数
    if (bottle < 2){
        return bottle;
    }
    return bottle + DrinkSoda(bottle/2+bottle%2)- bottle%2; //为什么要减去bottle%2，为0则说明每个瓶子都使用兑换掉了，如果为1则说明有一个瓶子没有使用掉则应在结果中减去一次
}