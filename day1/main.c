#include <stdio.h>

int main01(){
    int a = 10; // 0000 0000 0000 0000 0000 0000 0000 1010
    printf("%d\n",~a);  // 1111 1111 1111 1111 1111 1111 1111 0101 补
    // 1111 1111 1111 1111 1111 1111 1111 0100 反
    // 1000 0000 0000 0000 0000 0000 0000 1011 原
}

